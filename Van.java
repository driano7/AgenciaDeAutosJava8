/**
 * Clase abstracta de tipo pública para automóviles de tipo Van
 * @author Riaño Donovan
 * @author Tapia Alejandro
 * @author Villaseñor Luis 
 */
public abstract class Van{
    
    /**
    * Constructor por defecto de la clase Van
    */
    public Van(){}
        
    /**
     *Método abstracto de tipo público para la velocidad de una Van de la marca VolksWagen
     */
    public abstract void velocidadVolksWagen();

    /**
     *Método abstracto de tipo público para el rendimiento de una Van de la marca VolksWagen
     */
    public abstract void rendimientoVolksWagen();

    /**
     *Método abstracto de tipo público para el precio de una Van de la marca VolksWagen
     */
    public abstract void precioVolksWagen();

    /**
     *Método abstracto de tipo público para el sistema de una Van de la marca VolksWagen
     */
    public abstract void sistemaVolksWagen();
	
    /**
     *Método abstracto de tipo público para el control de una Van de la marca VolksWagen
     */
    public abstract void controlVolksWagen();	

    /**
     *Método abstracto de tipo público para el equipamiento de una Van de la marca VolksWagen
     */
    public abstract void equipadoVolksWagen();

    /**
     *Método abstracto de tipo público para la velocidad de una Van de la marca Dodge
     */
    public abstract void velocidadDodge();

    /**
     *Método abstracto de tipo público para el rendimiento de una Van de la marca Dodge
     */
    public abstract void rendimientoDodge();

    /**
     *Método abstracto de tipo público para el precio de una Van de la marca Dodge
     */
    public abstract void precioDodge();

    /**
     *Método abstracto de tipo público para el sistema de una Van de la marca Dodge
     */
    public abstract void sistemaDodge();
	
    /**
     *Método abstracto de tipo público para el control de una Van de la marca Dodge
     */
    public abstract void controlDodge();	

    /**
     *Método abstracto de tipo público para el equipamiento de una Van de la marca Dodge
     */
    public abstract void equipadoDodge();

    /**
     *Método abstracto de tipo público para la velocidad de una Van de la marca GMC
     */
    public abstract void velocidadGMC();

    /**
     *Método abstracto de tipo público para el rendimiento de una Van de la marca GMC
     */
    public abstract void rendimientoGMC();

    /**
     *Método abstracto de tipo público para el precio de una Van de la marca GMC
     */
    public abstract void precioGMC();
	
    /**
     *Método abstracto de tipo público para el control de una Van de la marca GMC
     */
    public abstract void controlGMC();	

    /**
     *Método abstracto de tipo público para el equipamiento de una Van de la marca GMC
     */
    public abstract void equipadoGMC();


}